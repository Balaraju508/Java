//3.Write a program to perform simple math based on the user inputs by using Switch condition(+ , - , * , /)//



class C{
	public static void main(String[] args)
	{
		int b=2;
		switch(b){
			case 1:
				System.out.println(b+2);
			case 2:
				System.out.println(b-1);
			case 3:
				System.out.println(b*6);
			case 4:
				System.out.println(b/2);
		}

	}
}